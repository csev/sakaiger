<h3>Notes</h3>
From Harriet: The original concept of a Sakaiger was mainly a silly pun thought up by
those crazy folks at CARET, Cambridge. We have intermittently talked about
other Sakai related animals (Sakainocerous, for example), but none of them
really caught on as the Sakaiger did. We wanted to have a real
implementation of a Sakaiger, and Andy Thornton drew one, which ended up
looking rather like the tiger from the Weebls cartoon 'Kenya'.  
Andy also bought a Weebl tiger and lion (and t-shirt) for the office.  We
customised our Weebl tiger a bit with a Sakai badge in its ear... The real
life Sakaigers as realised by Chuck don't look much like the Weebl one
(except they're clearly both meant to be tigers) - the Sakaiger is much
taller and thinner and is smily.
