<h3>Sakaiger Facts</h3>
<ul>
<li>Plush Sakaigers are in very limited supply given to those with significant 
contribution/leadership in Sakai QA Activity
<li>The first Sakaiger is larger than the rest and was given to Megan May - 
the Sakai QA director.
<li>Sakaigers were first sighted in Amsterdam on June 13, 2007 at the Sakai Conference.
<li>
Follow the worldwide travels of the Sakaiger
<a href=http://www.flickr.com/photos/tags/sakaiger/>
via Flickr</a>.  Please upload your Sakaiger photos and tag them with the "sakaiger" tag.
<li>Andrew Thornton of CARET / Cambridge designed the original Sakaiger image.
<li>Each stuffed Sakaiger is hand sewn in Perry, MI
(<a href=http://maps.google.com/maps?f=q&hl=en&q=n+church+st+vernon+mi&sll=42.939397,-84.032364&sspn=0.018693,0.025706&ie=UTF8&om=1&ll=42.940214,-84.030819&spn=0.037386,0.051413&z=14&iwloc=addr target=_new>Map</a>).  
The stuffed Sakaiger is based on a Simplicity 3779 pattern for a stuffed rabbit (www.simplicity.com) - the pattern was adapted to make the Sakaiger more tiger-like.
<li>It is not possible to purchase a Sakaiger at this time - they are only available as gifts.
<li>A relative of the Sakaiger - the 
<a href=http://www.weebls-stuff.com/toons/kenya/>
Kenya Tiger</a> from 
<a href=http://www.weebls-stuff.com/>Weebls</a>.  Please visit the site, view the
video and buy some of the totally sweet Weebl Stuff.
</ul>
