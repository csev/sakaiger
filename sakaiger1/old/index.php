<h3>The Legend of the Sakaiger</h3>
(Under construction)
<p>
The Sakaiger is a happy dancing creature.  It is the logo of the hip, cool, underground, karaoke singing, 
silly elements of the Sakai Community (that is pretty much everyone).  The Sakaiger is very much part of the lore
of the Sakai Quality Assurance effort.
